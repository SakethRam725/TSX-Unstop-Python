# This script calculates the area and perimeter of a rectangle.

# Define variables for length and width
length = 10  # You can change this value as needed
width = 5  # You can change this value as needed

# Calculate the area of the rectangle
area = length * width

# Calculate the perimeter of the rectangle
perimeter = 2 * (length + width)

# Print the results
print("Rectangle dimensions:")
print("Length:", length)
print("Width:", width)
print("Area:", area)
print("Perimeter:", perimeter)
